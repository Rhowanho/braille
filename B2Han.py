def String_to_list(str):
    check_list = False
    list1 = []
    string = ""
    
    for i in str:
        if check_list == False:
            if i in '\'':
                check_list = True
        
        elif check_list == True:
            if i not in '\'':
                string = string + i
            elif i in '\'':
                check_list = False
                list1.append(string)
                string = ""
            continue
    return list1
def text(String):
    index = 0
    data = []
    check = False
    
    text = String_to_list(String)

    for i in text:
        if i in MATCH_H2B_YEON_BR:
            data.append(MATCH_H2B_YEON[MATCH_H2B_YEON_BR[i]])
            
        elif i in MATCH_H2B_ABB :
            data.append(MATCH_H2B_ABB[i])
            
        elif i in "000000":
            data.append(" ")
            
        elif i in MATCH_H2B_MARK:
            data.append(MATCH_H2B_MARK[i])
            
        else:
            start = 0
            end = 6
            for j in range(len(i)//6):
                if len(i) == 6:
                    if i in MATCH_H2B_CHO:
                        data.append('')
                    else:
                        data.append("110110")
                data.append(i[start:end])
                start = end
                end += 6
    
    new_data=[]
    for i in data:
        if check == True:
            check = False
            index =  index + 1
            continue
        
        if i in MATCH_H2B_CHO:
            if i in '000001':
                if data[index + 1] in MATCH_H2B_CHO :
                    new_data.append(MATCH_H2B_CHO[i+data[index + 1]])
                    check = True
                else:
                    new_data.append(MATCH_H2B_CHO[i])
            else:
                new_data.append(MATCH_H2B_CHO[i])
        elif i in MATCH_H2B_JOONG:                
            if(data[index-1] not in MATCH_H2B_CHO):
                new_data.append("ㅇ")
